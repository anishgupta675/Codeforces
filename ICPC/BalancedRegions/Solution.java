import java.io.*;
import java.util.*;
import java.lang.*;
import java.math.*;
import java.text.*;
import java.security.*;
class Solution {
	public static void main(String[] args) throws IOException {
		Solution sol = new Solution();
		DataInputStream in = new Solution();
		int g = Integer.parseInt(in.readLine());
		for(int i = 0; i < g; i++) {
			int n = Integer.parseInt(in.readLine());
			int[] node = new int[n];
			for(int j = 0; j < n; j++)
				node[j] = Integer.parseInt(in.readLine());
			int k = Integer.parseInt(in.readLine());
			int[] mark = new int[k];
			for(int l = 0; l < k; l++)
				mark[l] = Integer.parseInt(in.readLine());
			int m = Integer.parseInt(in.readLine());
			Pair<Integer, Integer> edge = new Pair<Integer, Integer>();
			for(int o = 0; o < m; o++) {
				int node_x = Integer.parseInt(in.readLine());
				int node_y = Integer.parseInt(in.readLine());
				edge.add(new Pair<Integer, Integer> (node_x, node_y));
			}
		}
	}
}
